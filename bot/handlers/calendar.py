"""Calendar handler."""

from datetime import datetime
from aiogram import Router, F
from aiogram.types import CallbackQuery, InlineKeyboardMarkup, InlineKeyboardButton
from sqlalchemy.orm import Session
from sqlalchemy import desc

from config.database import SessionLocal
from database.models import User, SubscriptionType, CalendarEntry

from bot.keyboards.main_menu import get_main_menu_keyboard

router = Router()


@router.callback_query(F.data == "calendar")
async def calendar_callback(callback: CallbackQuery, user: User):
    """Handle calendar callback."""
    
    db = SessionLocal()
    try:
        # Get current month calendar
        current_month = datetime.now().month
        current_year = datetime.now().year
        
        calendar_entry = db.query(CalendarEntry).filter(
            CalendarEntry.month == current_month,
            CalendarEntry.year == current_year
        ).order_by(desc(CalendarEntry.created_at)).first()
        
        if not calendar_entry:
            # No calendar data available
            no_data_text = """üìÖ *–ö–∞–ª–µ–Ω–¥–∞—Ä—å —Å—Ç–æ–∫–µ—Ä–∞*\n\n–ö–∞–ª–µ–Ω–¥–∞—Ä—å –Ω–∞ —Ç–µ–∫—É—â–∏–π –º–µ—Å—è—Ü –ø–æ–∫–∞ –Ω–µ –≥–æ—Ç–æ–≤\\.\n\n‚ÑπÔ∏è –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –≤ —ç—Ç–æ–º —Ä–∞–∑–¥–µ–ª–µ –æ–±–Ω–æ–≤–ª—è–µ—Ç—Å—è *–µ–∂–µ–º–µ—Å—è—á–Ω–æ*\\. –¢–µ–±–µ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω—É–∂–Ω–æ –¥–µ–ª–∞—Ç—å ‚Äî –≤ –Ω–∞—á–∞–ª–µ –º–µ—Å—è—Ü–∞ –∑–¥–µ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–æ—è–≤–∏—Ç—Å—è –Ω–æ–≤–∞—è –ø–æ–¥–±–æ—Ä–∫–∞\\.\n\nüìå *–°–æ–≤–µ—Ç:* –∑–∞–≥—Ä—É–∂–∞–π —Å–µ–∑–æ–Ω–Ω—ã–µ —Ç–µ–º—ã –∑–∞ 4‚Äì8 –Ω–µ–¥–µ–ª—å –¥–æ —Å–æ–±—ã—Ç–∏—è\\. –¢–∞–∫ –æ–Ω–∏ —É—Å–ø–µ—é—Ç –≤—ã–π—Ç–∏ –≤ —Ç–æ–ø –∏ –¥–∞—Ç—å –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã\\."""
            
            await callback.message.edit_text(
                no_data_text,
                reply_markup=get_main_menu_keyboard(user.subscription_type)
            )
            await callback.answer()
            return
        
        # Parse calendar content
        load_now_themes = calendar_entry.load_now_themes or []
        prepare_themes = calendar_entry.prepare_themes or []
        
        if user.subscription_type == SubscriptionType.FREE:
            # Show limited calendar for FREE users
            calendar_text = f"""üìÖ *–ö–∞–ª–µ–Ω–¥–∞—Ä—å —Å—Ç–æ–∫–µ—Ä–∞*\n\n‚ÑπÔ∏è –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –≤ —ç—Ç–æ–º —Ä–∞–∑–¥–µ–ª–µ –æ–±–Ω–æ–≤–ª—è–µ—Ç—Å—è *–µ–∂–µ–º–µ—Å—è—á–Ω–æ*\\. –¢–µ–±–µ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω—É–∂–Ω–æ –¥–µ–ª–∞—Ç—å ‚Äî –≤ –Ω–∞—á–∞–ª–µ –º–µ—Å—è—Ü–∞ –∑–¥–µ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–æ—è–≤–∏—Ç—Å—è –Ω–æ–≤–∞—è –ø–æ–¥–±–æ—Ä–∫–∞\\.\n\n‚ú® *–ö–∞–ª–µ–Ω–¥–∞—Ä—å —Å—Ç–æ–∫–µ—Ä–∞ –Ω–∞ {calendar_entry.month}.{calendar_entry.year}* ‚ú® —Å–æ–∫—Ä–∞—â–µ–Ω–Ω—ã–π\n\n*–ì—Ä—É–∑–∏—Ç—å —Å–µ–π—á–∞—Å:*
{chr(10).join([f"‚Ä¢ {theme}" for theme in load_now_themes[:1]])}\n\n*–ì–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å / –≥–æ—Ç–æ–≤–∏—Ç—å—Å—è:*
{chr(10).join([f"‚Ä¢ {theme}" for theme in prepare_themes[:1]])}\n\nüìå *–°–æ–≤–µ—Ç:* –∑–∞–≥—Ä—É–∂–∞–π —Å–µ–∑–æ–Ω–Ω—ã–µ —Ç–µ–º—ã –∑–∞ 4‚Äì8 –Ω–µ–¥–µ–ª—å –¥–æ —Å–æ–±—ã—Ç–∏—è\\. –¢–∞–∫ –æ–Ω–∏ —É—Å–ø–µ—é—Ç –≤—ã–π—Ç–∏ –≤ —Ç–æ–ø –∏ –¥–∞—Ç—å –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã\\."""
            
            keyboard = [
                [
                    InlineKeyboardButton(text="üèÜ –ü–µ—Ä–µ–π—Ç–∏ –Ω–∞ PRO", callback_data="upgrade_pro")
                ],
                [
                    InlineKeyboardButton(text="üìä –°—Ä–∞–≤–Ω–∏—Ç—å Free –∏ PRO", callback_data="compare_free_pro")
                ],
                [
                    InlineKeyboardButton(text="‚Ü©Ô∏è –ù–∞–∑–∞–¥ –≤ –º–µ–Ω—é", callback_data="main_menu")
                ]
            ]
            
        else:
            # Show full calendar for PRO/ULTRA users
            calendar_text = f"""üìÖ *–ö–∞–ª–µ–Ω–¥–∞—Ä—å —Å—Ç–æ–∫–µ—Ä–∞*\n\n‚ÑπÔ∏è –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –≤ —ç—Ç–æ–º —Ä–∞–∑–¥–µ–ª–µ –æ–±–Ω–æ–≤–ª—è–µ—Ç—Å—è *–µ–∂–µ–º–µ—Å—è—á–Ω–æ*\\. –¢–µ–±–µ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω—É–∂–Ω–æ –¥–µ–ª–∞—Ç—å ‚Äî –≤ –Ω–∞—á–∞–ª–µ –º–µ—Å—è—Ü–∞ –∑–¥–µ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–æ—è–≤–∏—Ç—Å—è –Ω–æ–≤–∞—è –ø–æ–¥–±–æ—Ä–∫–∞\\.\n\n‚ú® *–ö–∞–ª–µ–Ω–¥–∞—Ä—å —Å—Ç–æ–∫–µ—Ä–∞ –Ω–∞ {calendar_entry.month}.{calendar_entry.year}* ‚ú®\n\n*–û–±—â–µ–µ –æ–ø–∏—Å–∞–Ω–∏–µ —Å–µ–∑–æ–Ω–∞:*
{calendar_entry.description}\n\n*–ì—Ä—É–∑–∏—Ç—å —Å–µ–π—á–∞—Å* \\(—Ç–æ, —á—Ç–æ —É–∂–µ –¥–æ–ª–∂–Ω–æ —É—Ö–æ–¥–∏—Ç—å –Ω–∞ —Å—Ç–æ–∫–∏\\):
{chr(10).join([f"‚Ä¢ {theme}" for theme in load_now_themes])}\n\n*–ì–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å / –≥–æ—Ç–æ–≤–∏—Ç—å—Å—è* \\(—Ç–æ, —á—Ç–æ –±—É–¥–µ—Ç –≤–æ—Å—Ç—Ä–µ–±–æ–≤–∞–Ω–æ —á–µ—Ä–µ–∑ 1\\-2 –º–µ—Å—è—Ü–∞\\):
{chr(10).join([f"‚Ä¢ {theme}" for theme in prepare_themes])}\n\nüìå *–°–æ–≤–µ—Ç:* –∑–∞–≥—Ä—É–∂–∞–π —Å–µ–∑–æ–Ω–Ω—ã–µ —Ç–µ–º—ã –∑–∞ 4‚Äì8 –Ω–µ–¥–µ–ª—å –¥–æ —Å–æ–±—ã—Ç–∏—è\\. –¢–∞–∫ –æ–Ω–∏ —É—Å–ø–µ—é—Ç –≤—ã–π—Ç–∏ –≤ —Ç–æ–ø –∏ –¥–∞—Ç—å –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã\\."""
            
            keyboard = [
                [
                    InlineKeyboardButton(text="‚Ü©Ô∏è –ù–∞–∑–∞–¥ –≤ –º–µ–Ω—é", callback_data="main_menu")
                ]
            ]
        
        await callback.message.edit_text(
            calendar_text,
            reply_markup=InlineKeyboardMarkup(inline_keyboard=keyboard)
        )
        
    finally:
        db.close()
    
    await callback.answer()
